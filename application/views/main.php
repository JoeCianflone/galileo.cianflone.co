					<h2>Obituary</h2>
					<p>Galileo F. Cianflone, 86, of Long Branch, died Monday, May 30, 2011, at home. He was born in Miglierina, Italy to the late Thomas and Carolina Cianflone. He was one of 7 children. Galileo attended school in Miglierina, learned the trade of Cabinet Maker and the Art of Music. At the age of 17, he enrolled in the Carabineri and was assigned to Florence headquarters. In 1943, he was sent to Yugoslavia from Florence. When Italy decided to change their course in the war, Galileo volunteered his services against Germany in the Partisan Company Garibaldi. In 1946, Galileo returned home to Miglierina as a Lieutenant. He met Maria Anastasio and was married on Dec. 2, 1948. He was active in his hometown. He was treasurer of the Municipal Band; president of the Italian Veterans Association; president of A.C. L.I., A National Union; and coordinator of the Christian Democratic Party. In December 1953, Galileo came to the U.S. and in 1959 he opened his business, Leo's Cabinet Shop on High Street. In 1974, he was employed by the Long Branch Board of Education as a foreman of the Maintenance Department until he retired in 1993. From 1991 to 1995, he served as a member representing the City of Long Branch at the Long Branch Sewerage Authority. Galileo has been an active member of the Amerigo Vespucci Society for over 50 years and has served in the capacity of every office including past president.</p>
					<p>Surviving are his wife of 62 years, Maria Anastasio Cianflone; a son and daughter-in-law, Thomas and Donna Cianflone of Long Branch; a daughter and son-in-law, Carol and Gary Mennie of Spring Lake Heights; 2 sisters, Giuseppina and Felicia of Italy; and his pride and joy, his 2 grandchildren, Joseph and Jasmine Cianflone and Anthony Mennie; and a great-grandson, Jacob Thomas Cianflone.</p>
					<p>Entombment will be at Woodbine Mausoleum. In lieu of flowers, donations may be made to the St. Jude Children's Research Hospital , 501 St. Jude Place, Memphis, TN 38105. The family invites you to leave an online condolence online.</p>
